     nf(3,nm)=ic
     ic=ic+1
     nf(2,nm)=ic
   END DO
   nm=nm+1
   nf(:,nm)=0
 END DO
!all other layers
DO k=1,nze-1
  DO i=1,2*nye
    nm=nm+1
    nf(1,nm)=0
    ic=ic+1
    nf(3,nm)=ic
    ic=ic+1
    nf(2,nm)=ic
  END DO
  nm=nm+1
  nf(:,nm)=0
  DO j=1,nx1
    DO i=1,nye
      nm=nm+1
      ic=ic+1
      nf(1,nm)=ic
      ic=ic+1
      nf(3,nm)=ic
      ic=ic+1
      nf(2,nm)=ic
    END DO
    nm=nm+1
    nf(:,nm)=0
    DO i=1,2*nye
      nm=nm+1
      ic=ic+1
      nf(1,nm)=ic
      ic=ic+1
      nf(3,nm)=ic
      ic=ic+1
      nf(2,nm)=ic
    END DO
    nm=nm+1
    nf(:,nm)=0
  END DO
  DO j=1,nx2
    DO i=1,ny2
      nm=nm+1
      ic=ic+1
      nf(1,nm)=ic
      ic=ic+1
      nf(3,nm)=ic
      ic=ic+1
      nf(2,nm)=ic
    END DO
    nm=nm+1
    nf(:,nm)=0
    DO i=1,2*ny2
      nm=nm+1
      IF(j==nx2)THEN
        nf(1,nm)=0
      ELSE
        ic=ic+1
        nf(1,nm)=ic
      END IF
      ic=ic+1
      nf(3,nm)=ic
      ic=ic+1
      nf(2,nm)=ic
    END DO
    nm=nm+1
    nf(:,nm)=0
  END DO
! middle layers
  DO i=1,nye
    nm=nm+1
    nf(1,nm)=0
    ic=ic+1
    nf(3,nm)=ic
    ic=ic+1
    nf(2,nm)=ic
  END DO
  nm=nm+1
  nf(:,nm)=0
  DO j=1,nx1
    DO i=1,nye
      nm=nm+1
      ic=ic+1
      nf(1,nm)=ic
      ic=ic+1
      nf(3,nm)=ic
      ic=ic+1
      nf(2,nm)=ic
    END DO
    nm=nm+1
    nf(:,nm)=0
  END DO
  DO j=1,nx2
    DO i=1,ny2
      nm=nm+1
      IF(j==nx2)THEN
        nf(1,nm)=0
      ELSE
        ic=ic+1
        nf(1,nm)=ic
