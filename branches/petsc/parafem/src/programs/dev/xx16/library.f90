       ic=ic+1
       nf(1,nm)=ic
       ic=ic+1
       nf(2,nm)=ic
     END IF
   END DO
   nm=nm+1
   nf(:,nm)=0
   DO i=1,2*ny2
     nm=nm+1
     nf(:,nm)=0
     IF(ifix==1)THEN
       IF(j<nx2)THEN
         ic=ic+1
         nf(1,nm)=ic
       END IF 
       ic=ic+1
       nf(2,nm)=ic
     END IF
   END DO
   nm=nm+1
   nf(:,nm)=0
 END DO    
!
!                       boundary conditions for the middle layer
!
 DO i=1,nye
   nm=nm+1
   nf(1,nm)=0
   ic=ic+1
   nf(3,nm)=ic
   ic=ic+1
   nf(2,nm)=ic
