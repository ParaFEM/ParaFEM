SUBROUTINE exc_nods(noexe,exele,g_num,totex,ntote,nf)
!
! This subroutine forms the nodes removed in an excavation lift.
!
 IMPLICIT NONE
 INTEGER,INTENT(IN)::noexe,exele(:),g_num(:,:)
 INTEGER,INTENT(IN OUT)::totex(:),ntote,nf(:,:)
 INTEGER::i,jj,k,iel,modex,nodex,ncheck,nels,nod
 nels=UBOUND(g_num,2)
 nod=UBOUND(g_num,1)
 ntote=ntote+noexe
 totex(ntote-noexe+1:ntote)=exele
 DO i=1,noexe
   DO k=1,8
     nodex=0
     ncheck=g_num(k,exele(i))
     DO iel=1,nels
       modex=0
       DO jj=1,ntote
         IF(iel==totex(jj))THEN
           modex=1
           EXIT
         END IF
       END DO
       IF(modex==1)CYCLE
       DO jj=1,nod
         IF(ncheck==g_num(jj,iel))THEN
           nodex=1
           EXIT
         END IF
       END DO
       IF(nodex==1)EXIT
     END DO
     IF(nodex==0)nf(:,ncheck)=0
   END DO
 END DO
RETURN
END SUBROUTINE exc_nods

SUBROUTINE emb_3d_bc(ifix,nx1,nx2,ny1,ny2,nze,nf)
!
! This subroutine generates the nf array for a 3-d slope geometry.
! Side boundary conditions:
! smooth-smooth  ifix=1  (for checking against plane-strain)
! rough -smooth  ifix=2  (for centerline symmetry)
! rough -rough   ifix=3
!
 IMPLICIT NONE
 INTEGER,INTENT(IN)::nx1,nx2,ny1,ny2,nze,ifix
 INTEGER,INTENT(OUT)::nf(:,:)
 INTEGER::nm,ic,i,j,k,nye
 nye=ny1+ny2
 nm=0
 ic=0
!
!                       boundary condition for the front face
!
 DO i=1,2*nye
   nm=nm+1
   nf(:,nm)=0
   IF(ifix==1)THEN
     ic=ic+1
     nf(2,nm)=ic
   END IF
 END DO
 nm=nm+1
 nf(:,nm)=0
 DO j=1,nx1
   DO i=1,nye
     nm=nm+1
     nf(:,nm)=0
     IF(ifix==1)THEN
       ic=ic+1
       nf(1,nm)=ic
       ic=ic+1
