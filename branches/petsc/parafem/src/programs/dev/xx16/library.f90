END DO
!
!                       boundary conditions for back face
!
 DO i=1,2*nye
   nm=nm+1
   nf(:,nm)=0
   IF(ifix==1.OR.ifix==2)THEN
     ic=ic+1
     nf(2,nm)=ic
   END IF
 END DO
 nm=nm+1
 nf(:,nm)=0
 DO j=1,nx1
   DO i=1,nye
     nm=nm+1
     nf(:,nm)=0
     IF(ifix==1.OR.ifix==2)THEN
       ic=ic+1
       nf(1,nm)=ic
       ic=ic+1
       nf(2,nm)=ic
     END IF
   END DO
   nm=nm+1
   nf(:,nm)=0
   DO i=1,2*nye
     nm=nm+1
     nf(:,nm)=0
     IF(ifix==1.OR.ifix==2)THEN
       ic=ic+1
       nf(1,nm)=ic
       ic=ic+1
       nf(2,nm)=ic
     END IF
   END DO
   nm=nm+1
   nf(:,nm)=0
 END DO
 DO j=1,nx2
   DO i=1,ny2
     nm=nm+1
     nf(:,nm)=0
     IF(ifix==1.OR.ifix==2)THEN
       ic=ic+1
       nf(1,nm)=ic
       ic=ic+1
       nf(2,nm)=ic
     END IF
   END DO
   nm=nm+1
   nf(:,nm)=0
   DO i=1,2*ny2
     nm=nm+1
     nf(:,nm)=0
     IF(ifix==1.OR.ifix==2)THEN
       IF(j<nx2)THEN
         ic=ic+1
         nf(1,nm)=ic
       END IF 
       ic=ic+1
       nf(2,nm)=ic
     END IF
   END DO
   nm=nm+1
   nf(:,nm)=0
 END DO    
RETURN
END SUBROUTINE emb_3d_bc

SUBROUTINE emb_3d_geom(iel,nx1,nx2,ny1,ny2,nze,w1,s1,w2,h1,h2,d1,coord,num)
!
! This subroutine forms the nodal coordinates and numbering for a 3-d
! slope of 20-node hexahedra. Nodes and elements numbered in xz planes
! going in the y-direction.
!
 IMPLICIT NONE
 INTEGER,PARAMETER::iwp=SELECTED_REAL_KIND(15)
 REAL(iwp),INTENT(IN)::w1,s1,w2,h1,h2, d1
 INTEGER,INTENT(IN)::iel,nx1,nx2,ny1,ny2,nze
 REAL(iwp),INTENT(OUT)::coord(:,:)
 INTEGER,intent(OUT)::num(:)
 INTEGER::nxe,nye,nc,nt,nn_mid,nn_main,ip,iq,is,nd,ns
 REAL(iwp)::facx,facy,facz,facb,facs,frh,zero=0.0_iwp,pt5=0.5_iwp
!
!                       basic variable initialization
!
 nxe=nx1+nx2
 nye=ny1+ny2
!number of elements in a slice
 ns=nx1*nye+nx2*ny2
!number of elements in sloped region
 nt=nx1*ny1
!number of nodes in main sloped region
 nc=(3*nye+2)*nx1+2*ny1
!number of nodes in mid sloped region
 nd=(nye+1)*nx1+ny1
!number of nodes in a middle layer
 nn_mid=(nye+1)*(nx1+1)+(ny2+1)*nx2
!number of nodes in a main layer
 nn_main=(3*nye+2)*nx1+2*nye+1+(3*ny2+2)*nx2
!
 facx=s1/ny1
 facy=h1/ny1
 facz=d1/nze
 facb=(w1+s1)/nx1
 facs=zero
 IF(ny2/=0)facs=h2/ny2
 frh=zero
 IF(nx2/=0)frh=w2/nx2
!
!                       determine element index values (ip, iq, is)
!
 is=(iel-1)/ns+1
 IF(iel-(is-1)*ns<=nt)THEN !if it is in sloped region
   iq=(iel-(is-1)*ns-1)/nx1+1
   ip=iel-(is-1)*ns-(iq-1)*nx1
 ELSE                      !if it is in unsloped region
   iq=(iel-(is-1)*ns-nt-1)/nxe+ny1+1
   ip=iel-(is-1)*ns-nt-(iq-ny1-1)*nxe
 END IF
!
! Calculate node indices (num()) and coordinates (coord(,))
 IF(ip<=nx1)THEN !if iel is in sloped region
   num(1)=(ip-1)*(3*nye+2)+2*iq+1+(is-1)*(nn_mid+nn_main)
   num(2)=num(1)-1
   num(3)=num(1)-2
   num(4)=(ip-1)*(3*nye+2)+2*nye+iq+1+(is-1)*(nn_mid+nn_main)
   num(5)=ip*(3*nye+2)+2*iq-1+(is-1)*(nn_mid+nn_main)
   num(6)=num(5)+1
   num(7)=num(5)+2
   num(8)=num(4)+1
   num(9)=(nn_main+(ip-1)*(nye+1)+iq+1)*is+(is-1)*                        &
     (nn_mid-(ip-1)*(nye+1)-iq-1)
   num(10)=num(9)-1
   num(11)=1+(nn_main+ip*(nye+1)+iq-1)*is+(is-1)*(nn_mid-ip*(nye+1)-iq+1)
   num(12)=num(11)+1
   num(13)=(ip-1)*(3*nye+2)+2*iq+1+is*(nn_mid+nn_main)
   num(14)=num(13)-1
   num(15)=num(13)-2
   num(16)=(ip-1)*(3*nye+2)+2*nye+iq+1+is*(nn_mid+nn_main)
   num(17)=ip*(3*nye+2)+2*iq-1+is*(nn_mid+nn_main)
   num(18)=num(17)+1
   num(19)=num(17)+2
   num(20)=num(16)+1
   IF(iq<=ny1)THEN ! if iel is in trapezoidal region
     coord(1,1)=(ip-1)*(w1+iq*facx)/nx1
     coord(3,1)=(ip-1)*(w1+(iq-1)*facx)/nx1
     coord(5,1)=ip*(w1+(iq-1)*facx)/nx1
     coord(7,1)=ip*(w1+iq*facx)/nx1
     coord(1,2)=-iq*facy
     coord(3,2)=-(iq-1)*facy
     coord(5,2)=-(iq-1)*facy
     coord(7,2)=-iq*facy
   ELSE            ! if iel is in rectangular region
     coord(1,1)=(ip-1)*facb
     coord(3,1)=(ip-1)*facb
     coord(5,1)=ip*facb
     coord(7,1)=ip*facb
     coord(1,2)=-h1-(iq-ny1)*facs
     coord(3,2)=-h1-(iq-ny1-1)*facs
     coord(5,2)=-h1-(iq-ny1-1)*facs
     coord(7,2)=-h1-(iq-ny1)*facs
   END IF
 ELSE              ! if iel is in unsloped region
   num(1)=(nn_mid+nn_main)*(is-1)+nc+(3*ny2+2)*(ip-nx1-1)+(iq-ny1)*2+1
   num(2)=num(1)-1
   num(3)=num(1)-2
   num(4)=(nn_mid+nn_main)*(is-1)+nc+(3*ny2+2)*(ip-nx1-1)+2*ny2+1+iq-ny1
   num(5)=(nn_mid+nn_main)*(is-1)+nc+(3*ny2+2)*(ip-nx1)+(iq-ny1)*2-1
   num(6)=num(5)+1
   num(7)=num(5)+2
   num(8)=num(4)+1
   num(9)=(nn_mid+nn_main)*(is-1)+nn_main+nd+(ny2+1)*(ip-nx1-1)+iq-ny1+1
   num(10)=num(9)-1
   num(11)=(nn_mid+nn_main)*(is-1)+nn_main+nd+(ny2+1)*(ip-nx1)+iq-ny1
   num(12)=num(11)+1
   num(13)=(nn_mid+nn_main)*is+nc+(3*ny2+2)*(ip-nx1-1)+(iq-ny1)*2+1
   num(14)=num(13)-1
   num(15)=num(13)-2
   num(16)=(nn_mid+nn_main)*is+nc+(3*ny2+2)*(ip-nx1-1)+2*ny2+1+iq-ny1
   num(17)=(nn_mid+nn_main)*is+nc+(3*ny2+2)*(ip-nx1)+(iq-ny1)*2-1
   num(18)=num(17)+1
   num(19)=num(17)+2
   num(20)=num(16)+1
!
   coord(1,1)=w1+s1+(ip-nx1-1)*frh
   coord(3,1)=coord(1,1)
   coord(5,1)=w1+s1+(ip-nx1)*frh
   coord(7,1)=coord(5,1)
   coord(1,2)=-h1-(iq-ny1)*facs
   coord(3,2)=-h1-(iq-ny1-1)*facs
   coord(5,2)=-h1-(iq-ny1-1)*facs
   coord(7,2)=-h1-(iq-ny1)*facs
 END IF
 coord(9,1)=coord(1,1)
 coord(10,1)=coord(3,1)
 coord(11,1)=coord(5,1)
 coord(12,1)=coord(7,1)
 coord(13,1)=coord(1,1)
 coord(15,1)=coord(3,1)
 coord(17,1)=coord(5,1)
 coord(19,1)=coord(7,1)
 coord(9,2) =coord(1,2)
 coord(10,2)=coord(3,2)
 coord(11,2)=coord(5,2)
 coord(12,2)=coord(7,2)
 coord(13,2)=coord(1,2)
 coord(15,2)=coord(3,2)
 coord(17,2)=coord(5,2)
 coord(19,2)=coord(7,2)
 coord(1:8:1,3)=-(is-1)*facz
 coord(9:12:1,3)=-(is*facz-facz*pt5)
 coord(13:20:1,3)=-is*facz    
 coord(2:6:2,:)=pt5*(coord(1:5:2,:)+coord(3:7:2,:))
 coord(14:18:2,:)=pt5*(coord(13:17:2,:)+coord(15:19:2,:))
 coord(8,:)=pt5*(coord(7,:)+coord(1,:))
 coord(20,:)=pt5*(coord(19,:)+coord(13,:))
RETURN
END SUBROUTINE emb_3d_geom

SUBROUTINE invar(stress,sigm,dsbar,theta)
!
! This subroutine forms the stress invariants in 2- or 3-d.
!
 IMPLICIT NONE
 INTEGER,PARAMETER::iwp=SELECTED_REAL_KIND(15)
 REAL(iwp),INTENT(IN)::stress(:)
 REAL(iwp),INTENT(OUT),OPTIONAL::sigm,dsbar,theta
 REAL(iwp)::sx,sy,sz,txy,dx,dy,dz,xj3,sine,s1,s2,s3,s4,s5,s6,ds1,ds2,ds3, &
   d2,d3,sq3,zero=0.0_iwp,small=1.e-10_iwp,one=1.0_iwp,two=2.0_iwp,       &
   three=3.0_iwp,six=6.0_iwp,thpt5=13.5_iwp
 INTEGER::nst 
 nst=UBOUND(stress,1)
 SELECT CASE(nst)
 CASE(4)
   sx=stress(1)
   sy=stress(2)
   txy=stress(3)
   sz=stress(4)
   sigm=(sx+sy+sz)/three
   dsbar=SQRT((sx-sy)**2+(sy-sz)**2+(sz-sx)**2+six*txy**2)/SQRT(two)
   IF(dsbar<small)THEN
     theta=zero
   ELSE
     dx=(two*sx-sy-sz)/three
     dy=(two*sy-sz-sx)/three
     dz=(two*sz-sx-sy)/three
     xj3=dx*dy*dz-dz*txy**2
     sine=-thpt5*xj3/dsbar**3
     IF(sine>=one)sine=one
     IF(sine<-one)sine=-one
     theta=ASIN(sine)/three
   END IF
 CASE(6)
   sq3=SQRT(three)
   s1=stress(1)  
   s2=stress(2)
   s3=stress(3) 
   s4=stress(4)
   s5=stress(5)
   s6=stress(6)
   sigm=(s1+s2+s3)/three
   d2=((s1-s2)**2+(s2-s3)**2+(s3-s1)**2)/six+s4*s4+s5*s5+s6*s6
   ds1=s1-sigm 
   ds2=s2-sigm  
   ds3=s3-sigm
   d3=ds1*ds2*ds3-ds1*s5*s5-ds2*s6*s6-ds3*s4*s4+two*s4*s5*s6
   dsbar=sq3*SQRT(d2)
   IF(dsbar<small)THEN
     theta=zero
   ELSE
     sine=-three*sq3*d3/(two*SQRT(d2)**3)
     IF(sine>=one)sine=one 
     IF(sine<-one)sine=-one 
     theta=ASIN(sine)/three
   END IF
 CASE DEFAULT
   WRITE(*,*)"wrong size for nst in invar"
 END SELECT
RETURN
END SUBROUTINE invar

SUBROUTINE mocouf(phi,c,sigm,dsbar,theta,f)
!
! This subroutine calculates the value of the yield function
! for a Mohr-Coulomb material (phi in degrees).
!
 IMPLICIT NONE
 INTEGER,PARAMETER::iwp=SELECTED_REAL_KIND(15)
 REAL(iwp),INTENT(IN)::phi,c,sigm,dsbar,theta   
 REAL(iwp),INTENT(OUT)::f
 REAL(iwp)::phir,snph,csph,csth,snth,one=1.0_iwp,d3=3.0_iwp,d4=4.0_iwp,   &
   d180=180.0_iwp
 phir=phi*d4*ATAN(one)/d180
 snph=SIN(phir) 
 csph=COS(phir) 
 csth=COS(theta)
 snth=SIN(theta)
 f=snph*sigm+dsbar*(csth/SQRT(d3)-snth*snph/d3)-c*csph
RETURN
END SUBROUTINE mocouf

SUBROUTINE mocouq(psi,dsbar,theta,dq1,dq2,dq3)
!
! This subroutine forms the derivatives of a Mohr-Coulomb potential
! function with respect to the three invariants (psi in degrees).
!
 IMPLICIT NONE
 INTEGER,PARAMETER::iwp=SELECTED_REAL_KIND(15)
 REAL(iwp),INTENT(IN)::psi,dsbar,theta
 REAL(iwp),INTENT(OUT)::dq1,dq2,dq3
 REAL(iwp)::psir,snth,snps,sq3,c1,csth,cs3th,tn3th,tnth,zero=0.0_iwp,     &
   pt49=0.49_iwp,pt5=0.5_iwp,one=1.0_iwp,d3=3.0_iwp,d4=4.0_iwp,           &
   d180=180.0_iwp
 psir=psi*d4*ATAN(one)/d180 
 snth=SIN(theta) 
 snps=SIN(psir)
 sq3=SQRT(d3)  
 dq1=snps
 if(ABS(snth).GT.pt49)THEN
   c1=one
   IF(snth.LT.zero)c1=-one
   dq2=(sq3*pt5-c1*snps*pt5/sq3)*sq3*pt5/dsbar 
   dq3=zero
 ELSE
   csth=COS(theta)
   cs3th=COS(d3*theta)
   tn3th=TAN(d3*theta)
   tnth=snth/csth
   dq2=sq3*csth/dsbar*((one+tnth*tn3th)+snps*(tn3th-tnth)/sq3)*pt5
   dq3=pt5*d3*(sq3*snth+snps*csth)/(cs3th*dsbar*dsbar)
 END IF
RETURN
END SUBROUTINE mocouq

SUBROUTINE formm(stress,m1,m2,m3)
!
! This subroutine forms the derivatives of the invariants with respect to
! stress in 2- or 3-d.
!
 IMPLICIT NONE
 INTEGER,PARAMETER::iwp=SELECTED_REAL_KIND(15)
 REAL(iwp),INTENT(IN)::stress(:)
 REAL(iwp),INTENT(OUT)::m1(:,:),m2(:,:),m3(:,:)
 REAL(iwp)::sx,sy,txy,tyz,tzx,sz,dx,dy,dz,sigm,zero=0.0_iwp,one=1.0_iwp,  &
   two=2.0_iwp,three=3.0_iwp,six=6.0_iwp,nine=9.0_iwp
 INTEGER::nst,i,j
 nst=UBOUND(stress,1)
 SELECT CASE(nst)
 CASE(4)
   sx=stress(1)
   sy=stress(2)
   txy=stress(3)
   sz=stress(4)
   dx=(two*sx-sy-sz)/three
   dy=(two*sy-sz-sx)/three
   dz=(two*sz-sx-sy)/three
   sigm=(sx+sy+sz)/three
   m1=zero
   m2=zero
   m3=zero
   m1(1,1:2)=one
   m1(2,1:2)=one
   m1(4,1:2)=one
   m1(1,4)=one
   m1(4,4)=one
   m1(2,4)=one
   m1=m1/nine/sigm
   m2(1,1)=two/three
   m2(2,2)=two/three
   m2(4,4)= two/three
   m2(2,4)=-one/three
   m2(4,2)=-one/three
   m2(1,2)=-one/three
   m2(2,1)=-one/three
   m2(1,4)=-one/three
   m2(4,1)=-one/three
   m2(3,3)=two
   m3(3,3)=-dz
   m3(1:2,3)=txy/three
   m3(3,1:2)=txy/three
   m3(3,4)=-two*txy/three
   m3(4,3)=-two*txy/three
   m3(1,1)=dx/three
   m3(2,4)=dx/three
   m3(4,2)=dx/three
   m3(2,2)=dy/three
   m3(1,4)=dy/three
   m3(4,1)=dy/three
   m3(4,4)=dz/three
   m3(1,2)=dz/three
   m3(2,1)=dz/three
 CASE(6)
   sx=stress(1)
   sy=stress(2)    
   sz=stress(3)
   txy=stress(4)  
   tyz=stress(5) 
   tzx=stress(6)
   sigm=(sx+sy+sz)/three
   dx=sx-sigm  
   dy=sy-sigm 
   dz=sz-sigm
   m1=zero
   m2=zero
   m1(1:3,1:3)=one/(three*sigm)
   DO i=1,3 
     m2(i,i)=two 
     m2(i+3,i+3)=six 
   END DO
   m2(1,2)=-one
   m2(1,3)=-one 
   m2(2,3)=-one
   m3(1,1)=dx
   m3(1,2)=dz 
   m3(1,3)=dy 
   m3(1,4)=txy  
   m3(1,5)=-two*tyz
   m3(1,6)=tzx 
   m3(2,2)=dy 
   m3(2,3)=dx 
   m3(2,4)=txy
   m3(2,5)=tyz 
   m3(2,6)=-two*tzx 
   m3(3,3)=dz
   m3(3,4)=-two*txy
   m3(3,5)=tyz 
   m3(3,6)=tzx
   m3(4,4)=-three*dz 
   m3(4,5)=three*tzx
   m3(4,6)=three*tyz
   m3(5,5)=-three*dx
   m3(5,6)=three*txy 
   m3(6,6)=-three*dy
   DO i=1,6 
     DO j=i+1,6
       m1(j,i)=m1(i,j) 
       m2(j,i)=m2(i,j)   
       m3(j,i)=m3(i,j)
     END DO
   END DO
   m1=m1/three
   m2=m2/three
   m3=m3/three
 CASE DEFAULT
   WRITE(*,*)"nst size not recognised in formm"
 END SELECT
RETURN   
END SUBROUTINE formm

SUBROUTINE contour(loads,g_coord,g_num,ned,argv,nlen,ips)
!
! This subroutine produces a PostScript output file "*.con" displaying
! a contour map of loads over the finite element mesh.
!
 IMPLICIT NONE
 INTEGER,PARAMETER::iwp=SELECTED_REAL_KIND(15)
 REAL(iwp),INTENT(IN):: loads(0:),g_coord(:,:)
 INTEGER,INTENT(IN)::g_num(:,:),ned,ips,nlen
 CHARACTER(*),INTENT(IN)::argv
 REAL(iwp)::xmin,xmax,ymin,ymax,width,height,scale=72,sxy,xo,yo
 REAL(iwp)::pmin,pmax,ratio,x12,y12,x23,y23,x34,y34,x41,y41,x1,y1
 REAL(iwp)::pt5=0.5_iwp,elvn=11.0_iwp,ept5=8.5_iwp,eight=8.0_iwp,         &
   onept5=1.5_iwp,fpt5=5.5_iwp
 LOGICAL::s12,s23,s34,s41,draw
 INTEGER,ALLOCATABLE::corner(:,:)
 REAL(iwp),ALLOCATABLE::cont(:)
 INTEGER::i,j,k,l,nn,nels,nci,nod,ns,i1,i2,j1,j2
!
 OPEN(ips,FILE=argv(1:nlen)//'.con')
!
!                       compute size of mesh
!
 nn=UBOUND(g_coord,2)
 nod= UBOUND(g_num,1)
 nels=UBOUND(g_num,2)
 xmin=g_coord(1,1)
 xmax=g_coord(1,1)
 ymin=g_coord(2,1)
 ymax=g_coord(2,1)
 DO i=2,nn
   IF(g_coord(1,i)<xmin)xmin=g_coord(1,i)      
   IF(g_coord(1,i)>xmax)xmax=g_coord(1,i)      
   IF(g_coord(2,i)<ymin)ymin=g_coord(2,i)      
   IF(g_coord(2,i)>ymax)ymax=g_coord(2,i)      
 END DO
 width =xmax-xmin
 height=ymax-ymin
!
!                       allow 1.5" margin minimum on each side of figure
!
!                       portrait mode 
!
 IF(height.GE.elvn/ept5*width)THEN 
!
!                       height governs the scale
!
   sxy=scale*eight/height
   xo=scale*pt5*(ept5-eight*width/height)
   yo=scale*onept5
 ELSE
!
!                       width governs the scale
!
   sxy=scale*fpt5/width
   xo=scale*onept5
   yo=scale*pt5*(elvn-fpt5*height/width)
 END IF
!
!                       find range of potentials and contour values
!
 nci=ned+1
 pmin=MINVAL(loads(1:))
 pmax=MAXVAL(loads(1:))
 ALLOCATE(cont(nci))
 cont(1)=pmin
 DO i=2,nci
   cont(i)=cont(i-1)+(pmax-pmin)/ned
 END DO
!
!                       start PostScript output
!
 WRITE(ips,'(a)')'%!PS-Adobe-1.0'
 WRITE(ips,'(a)')'%%DocumentFonts: none'
 WRITE(ips,'(a)')'%%Pages: 1'
 WRITE(ips,'(a)')'%%EndComments'
 WRITE(ips,'(a)')'/m {moveto} def'
 WRITE(ips,'(a)')'/l {lineto} def'
 WRITE(ips,'(a)')'/s {stroke} def'
 WRITE(ips,'(a)')'%%EndProlog'
 WRITE(ips,'(a)')'%%Page: 0 1'
 WRITE(ips,'(a)')'gsave'
!
 WRITE(ips,'(2f9.2,a)') xo, yo, ' translate'
 WRITE(ips,'(f9.2,a)') 1.0, ' setlinewidth'
!
!                       draw the mesh outline
!
 IF(nod==3.OR.nod==6.OR.nod==10.OR.nod==15)ns=3
 IF(nod==4.OR.nod==8.OR.nod==9)ns=4
 ALLOCATE(corner(ns,2))
 IF(nod== 3)corner=RESHAPE((/1,2,3,2,3,1/),(/3,2/))
 IF(nod== 6)corner=RESHAPE((/1,3,5,3,5,1/),(/3,2/))
 IF(nod==10)corner=RESHAPE((/1,4,7,4,7,1/),(/3,2/))
 IF(nod==15)corner=RESHAPE((/1,5,9,5,9,1/),(/3,2/))
 IF(nod== 4)corner=RESHAPE((/1,2,3,4,2,3,4,1/),(/4,2/))
 IF(nod== 8)corner=RESHAPE((/1,3,5,7,3,5,7,1/),(/4,2/))
 IF(nod== 9)corner=RESHAPE((/1,3,5,7,3,5,7,1/),(/4,2/))
 DO i=1,nels
   DO j=1,ns
     draw=.TRUE.
     i1=g_num(corner(j,1),i)
     i2=g_num(corner(j,2),i)
     DO k=1,nels
       DO l=1,ns
         j1=g_num(corner(l,1),k)
         j2=g_num(corner(l,2),k)
         IF((i1==j2).AND.(i2==j1))THEN
           draw=.FALSE.
           EXIT
         END IF
       END DO
       IF(.NOT.draw)EXIT
     END DO
     IF(draw)THEN
       x1=sxy*(g_coord(1,i1)-xmin)
       y1=sxy*(g_coord(2,i1)-ymin)
